static const char* kStarfieldSource =
    "// Hash function for pseudo-random values\n"
    "float hash(vec2 p) {\n"
    "    p = fract(p * vec2(123.34, 456.21));\n"
    "    p += dot(p, p + 45.32);\n"
    "    return fract(p.x * p.y);\n"
    "}\n"
    "\n"
    "// Single star layer\n"
    "float starLayer(vec2 uv, float scale, float speed) {\n"
    "    uv *= scale;\n"
    "    uv.y += iTime * speed;\n"
    "    vec2 id = floor(uv);\n"
    "    vec2 gv = fract(uv) - 0.5;\n"
    "\n"
    "    float h = hash(id);\n"
    "    float size = 0.02 + h * 0.03;\n"
    "    float brightness = 0.5 + 0.5 * sin(iTime * (1.0 + h * 3.0) + h * 6.28);\n"
    "    float d = length(gv - (vec2(h, fract(h * 34.0)) - 0.5) * 0.8);\n"
    "    float star = smoothstep(size, size * 0.2, d) * brightness;\n"
    "    return star;\n"
    "}\n"
    "\n"
    "void mainImage(out vec4 fragColor, in vec2 fragCoord)\n"
    "{\n"
    "    vec2 uv = (fragCoord - 0.5 * iResolution.xy) / iResolution.y;\n"
    "\n"
    "    // Dark background with subtle gradient\n"
    "    vec3 col = vec3(0.01, 0.01, 0.03) + uv.y * 0.02;\n"
    "\n"
    "    // Three parallax layers at different speeds and scales\n"
    "    col += vec3(0.6, 0.7, 1.0) * starLayer(uv, 8.0, 0.05);\n"
    "    col += vec3(0.8, 0.8, 1.0) * starLayer(uv, 16.0, 0.1);\n"
    "    col += vec3(1.0, 0.9, 0.8) * starLayer(uv, 32.0, 0.2);\n"
    "\n"
    "    fragColor = vec4(col, 1.0);\n"
    "}\n";

