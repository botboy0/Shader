void TemplatePicker::open()
{
    m_shouldOpen = true;
}

void TemplatePicker::render()
{
    if (m_shouldOpen) {
        ImGui::OpenPopup("New Shader Template");
        m_shouldOpen = false;
    }

    ImVec2 center = ImGui::GetMainViewport()->GetCenter();
    ImGui::SetNextWindowPos(center, ImGuiCond_Appearing, ImVec2(0.5f, 0.5f));
    ImGui::SetNextWindowSize(ImVec2(500, 400), ImGuiCond_Appearing);

    if (ImGui::BeginPopupModal("New Shader Template", nullptr, ImGuiWindowFlags_NoResize)) {
        ImGui::Text("Choose a shader template:");
        ImGui::Separator();
        ImGui::Spacing();

        for (auto& tmpl : m_templates) {
            ImGui::PushID(tmpl.name);
            if (ImGui::Selectable("", false, 0, ImVec2(0, 40))) {
                m_selectedSource = tmpl.source;
                m_hasSelection = true;
                fprintf(stderr, "[TemplatePicker] Selected: %s\n", tmpl.name);
                ImGui::CloseCurrentPopup();
            }
            ImGui::SameLine();
            ImGui::BeginGroup();
            ImGui::TextUnformatted(tmpl.name);
            ImGui::TextDisabled("%s", tmpl.description);
            ImGui::EndGroup();
            ImGui::PopID();
        }

        ImGui::Spacing();
        ImGui::Separator();
        if (ImGui::Button("Cancel", ImVec2(120, 0))) {
            ImGui::CloseCurrentPopup();
        }
        ImGui::EndPopup();
    }
}

std::string TemplatePicker::takeSelection()
{
    m_hasSelection = false;
    return std::move(m_selectedSource);
}

